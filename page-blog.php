                    <h2 class="main__title black">Overcoming Client Barriers with SEO Testing</h2>
                    <p>As someone who works in the space, I have seen many custom solutions developed from agency to agency over the years to tackle problems like the above. And, in talking to agency owners and SEOs, I can tell you 
                        it used to be much harder to do any kind of split-testing before the advent of SEO SaaS split-testing tools, like 
                        <a href="#" class="blog-page-post__article-list--link">SplitSignal</a>.
                    </p>
                    <p>Now, however, it’s as simple as coming up with a testable idea (your hypothesis), setting up a line of
                        JavaScript on your client’s website (via Google Tag Manager, something similar, or just drop the line directly into the head of the website code), set up a split test in the tool to 
                        measure your hypothesis.
                    </p>
                    <p>Read more about SEO split-testing: <a class="blog-page-post__article-list--link post__category" href="#">SEO A/B Split Testing 101</a></p>
                    <h2 class="main__title black">Why SEO Split-Testing?</h2>
                    <p>Running measurable tests on statistically significant quantities of traffic to your website can break down the barriers to having your recommendations implemented quickly and 
                        rapidly demonstrate the value of you as an individual or your agency as a whole.</p>
                    <p>Whether you have a positive or negative test, you can show value. A positive test can be prioritized for implementation based on the overall impact it’s going to have on the website. 
                        A 50% increase in traffic, for example, should be a pretty high priority for your client (and you). A negative test result isn’t a bad thing; it keeps you from wasting precious 
                        time and budget on implementing something that isn’t going to work. As you know, what works in one vertical doesn’t always work in another.</p>
                    <h2 class="main__title black">6 Best Practices for Split-Testing Your SEO Recommendations</h2>
                    <h3 class="post__title black">1. Set a hypothesis before you get started</h3>
                    <p>Once your testing tool is ready to go, you need a test you can configure and run. Developing a 
                        <a class="blog-page-post__article-list--link post__category" href="#">hypothesis</a> is simply asking a question. For example, will adding “Holidays 2021” (or your current year if 
                        you’re reading this in the future!) to your meta title <a class="blog-page-post__article-list--link post__category" href="#">improve the click-through rate 
                            to your products during the holiday season</a>?
                    </p>
                    <p>By choosing high-impact elements within your website (e.g., title tags, heading elements, meta descriptions, etc.), you can draft simple tests with significant rewards. 
                        However, if your test is too narrow, you may not be able to observe enough of an impact to draw any helpful conclusions.</p>
                    <p>Once you have your hypothesis at hand, you’ll be ready to start implementing your test because what you want to test should be clearly defined.</p>
                    <h3 class="post__title">2. Make sure your sample size is large enough</h3>
                    <p>If you aren’t familiar with statistics, one thing you need to know is any statistical test, like split-testing, needs to have a sample size large enough to be able to draw 
                        valid conclusions (sort of why you’d rely on a 5-star rating from thousands of restaurant patrons, versus one 5-star rating from a single visitor).&nbsp;</p>
                    <p>In most instances for SEO, your sample is going to be traffic or clicks to your pages from search engine users. There are a number of other external factors that can impact your 
                        sample, things causing major fluctuations in traffic. In general, these can be avoided by making sure your control group of pages and test group are comparably diversified. Also, 
                        with a diversified test group, you don’t even have to worry about algorithm changes in the middle of a test because it would affect each group evenly.</p>
